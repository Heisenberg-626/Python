colour_code=input("Enter the colour code:")
if colour_code=="R" or colour_code=="r":
    print("The colour is: Red")
elif colour_code=="G" or colour_code=="g":
    print("The colour is: Green")
elif colour_code=="B" or colour_code=="b":
    print("The colour is: Blue")
else:
    print("Wrong code")
